function mas_filtered = cyclic_conv(mas, h)
% Циклическая свертка для обработки периодических сигналов
% Физика: используется для фильтрации ЛЧМ-сигналов, которые имеют периодическую природу
% Программирование: реализация циклической свертки через линейную свертку с дополнением

   assert(length(mas) >= length(h))  % Проверка: сигнал должен быть не короче ядра свертки

   H = length(h);  % Длина ядра свертки
   % Дополнение сигнала для имитации цикличности (конец + весь сигнал + начало)
   mas_temp = conv([mas(end-H+1:end); mas; mas(1:H)], h, 'same');
   % Выделение центральной части, соответствующей циклической свертке
   mas_filtered = mas_temp(H+1 : H+length(mas));

end