function fid = cfloat32_to_txt(mas, filename)
% Конвертация комплексного массива float32 в текстовый файл
% Физика: экспорт данных для анализа в других программах или верификации
% Программирование: запись комплексных чисел в текстовый формат с высокой точностью

   fid = fopen([filename '.txt'], 'w');  % Открытие файла для записи
   N = length(mas);  % Определение длины массива
   for i = 1 : 1 : N-1
      % Запись действительной и мнимой части с высокой точностью (19 знаков после запятой)
      fprintf(fid,'%24.19ff, %24.19ff,\n', real(mas(i)), imag(mas(i)));
   end % for i
   % Последняя запись без запятой в конце
   fprintf(fid,'%24.19ff, %24.19ff\n', real(mas(N)), imag(mas(N)));
   fclose(fid);  % Закрытие файла

end % function